package pl.zietekmati.webappcalc;

public class Numbers {


    private String liczbaA;
    public String liczbaB;

    public Numbers(String liczbaA, String liczbaB) {
        this.liczbaA = liczbaA;
        this.liczbaB = liczbaB;
    }

    public String getLiczbaA() {
        return liczbaA;
    }

    public String getLiczbaB() {
        return liczbaB;
    }

}